#include <stdio.h>

int main(){
    int num1 = 45;
    float num2 = 45.55;
    double num3 = 45.555555;
    char ch = 'A';

    printf("Integer Number: %d\n", num1); // Integer Number: 45
    printf("Float Number: %f\n", num2); // Float Number: 45.550000
    printf("Double Number: %lf\n", num3); // Double Number: 45.555555
    printf("Character: %c\n", ch); // Character: A
}