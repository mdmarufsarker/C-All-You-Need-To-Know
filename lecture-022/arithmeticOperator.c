#include <stdio.h>

int main(){
    int a = 30;
    int b = 7;

    printf("Addition: %d\n", a + b); // 30 + 7 = 37
    printf("Subtraction: %d\n", a - b); // 30 - 7 = 23
    printf("Multiplication: %d\n", a * b); // 30 * 7 = 210
    printf("Division: %d\n", a / b); // 30 / 7 = 4
    printf("Modulus: %d\n", a % b); // 30 % 7 = 2
}